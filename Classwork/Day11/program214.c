#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   Display
//  Description     :   used to take string from user and prints individual charaters from it
//  Input           :   String
//  Output          :   Character
//  Author          :   Aditya Bhaskar Sanap
//  Date            :   27
//
////////////////////////////////////////////////////////////////////////


void Display(char * Str)
{
    printf("%c\n", *Str);
    Str++;
    printf("%c\n", *Str);
    Str++;
    printf("%c\n", *Str);
}

////////////////////////////////////////////////////////////////////////
//
//  Entry point function : Main
//
////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter string: \n");
    scanf("%[^'\n']s", Arr);

    Display(Arr);

    return 0;
}// End of Main
