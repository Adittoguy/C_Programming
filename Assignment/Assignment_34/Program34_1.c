#include <stdio.h>

////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   DisplayASCII
//  Description     :   Used to display all the ascii value from table
//  Input           :   Integer
//  Output          :   ASCII values
//  Author          :   Aditya Bhaskar Sanap
//  Date            :   03/12/2025
//
////////////////////////////////////////////////////////////////////////

void DisplayASCII()
{
    int ch = '0';

    for(ch = '0'; ch <= 255; ch++)
    {
        printf("%c \n", ch);
    }
}

////////////////////////////////////////////////////////////////////////
//
//  Entry point function : Main
//
////////////////////////////////////////////////////////////////////////

int main()
{
    DisplayASCII();

    return 0;
}