#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   Display
//  Description     :   Recursive program to display Small Alphabets
//  Input           :   Characters   
//  Output          :   Characters
//  Author          :   Aditya Bhaskar Sanap
//  Date            :   15/12/2025
//
////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo < 'a')
    {
        return;
    }

    Display(iNo - 1);

    printf("%c\t", iNo);

}// End of Function

////////////////////////////////////////////////////////////////////////
//
//  Entry point function : Main
//
////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number of Character: ");
    scanf("%d", &iValue);

    Display('a' + iValue - 1);

    return 0;
}// End of Main