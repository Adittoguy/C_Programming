void Display(int iNo)
{
    if(iNo == 0)
    {
        return;
    }

    printf("*\t");

    Display(iNo - 1);
}// End of Function

////////////////////////////////////////////////////////////////////////
//
//  Entry point function : Main
//
////////////////////////////////////////////////////////////////////////

int main()
{
    Display(5);

    return 0;
}// End of Main