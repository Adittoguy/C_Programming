#include<stdio.h>

////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   Display
//  Description     :   Recursive program to display Capital Alphabets
//  Input           :   Characters   
//  Output          :   Characters
//  Author          :   Aditya Bhaskar Sanap
//  Date            :   15/12/2025
//
////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    if(ch < 'A')
    {
        return;
    }

    Display(ch - 1);

    printf("%c\t", ch);

}// End of Function

////////////////////////////////////////////////////////////////////////
//
//  Entry point function : Main
//
////////////////////////////////////////////////////////////////////////

int main()
{
    Display('F');

    return 0;
}// End of Main