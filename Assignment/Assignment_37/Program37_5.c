void StrRevX(char *str)
{
    char * temp = str;

    while(*str != '\0')
    {
        *str++;
    }

    while(str >= temp)
    {
        printf("%c", *str);
        str--;
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////
//
//  Entry point function : Main
//
////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];

    printf("Enter string: ");
    scanf("%[^'\n']s", arr);

    StrRevX(arr);

    // printf("Modified string is %s", arr);

    return 0;
}